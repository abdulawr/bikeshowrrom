package com.ss_technology.bikeshowroom.Container;

public class Message_Container {
    String id,subject,message,date,replay,replay_status;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getReplay() {
        return replay;
    }

    public void setReplay(String replay) {
        this.replay = replay;
    }

    public String getReplay_status() {
        return replay_status;
    }

    public void setReplay_status(String replay_status) {
        this.replay_status = replay_status;
    }
}
